/**
 * Как сдавать ДЗ с игрой (ничего менять не нужно):
 * — Откройте в браузере адрес сайта и в конце добавьте `/luck`
 *   Пример локально: http://localhost:8080/luck  (порт см. в vite.config)
 *   Пример на GitHub Pages: https://ВАШ-ЛОГИН.github.io/ИМЯ-РЕПО/luck
 * Это отдельная страница только с игрой — её и можно дать преподавателю.
 *
 * Что делает этот файл (только если разработчик тестирует варианты):
 * - `panel` — кнопка «Крутим удачу?» внутри проекта открывает игру поверх экрана (вариант A)
 * - `route` — та же кнопка ведёт на страницу `/luck` (вариант B)
 * Страница `/luck` работает всегда, при любом значении ниже.
 */
export type LuckyWheelEntryMode = "panel" | "route";

/** По умолчанию: игра открывается панелью по клику в приложении. Для ссылки ДЗ используйте `/luck` в браузере. */
export const LUCKY_WHEEL_ENTRY: LuckyWheelEntryMode = "panel";

/** Сколько оборотов за одну сессию (ТЗ: 3–4). */
export const LUCKY_SPINS_PER_GAME = 4;
